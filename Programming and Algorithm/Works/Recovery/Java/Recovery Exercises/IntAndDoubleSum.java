public class IntAndDoubleSum { // Start of the class
    public static void main(String[] args) { // Main method: program starts here

        int wholeNumber = 5; // Integer value
        double decimalNumber = 2.5; // Decimal (double) value
        double sum = wholeNumber + decimalNumber; // Add int and double, result is double

        System.out.println(wholeNumber + " + " + decimalNumber + " = " + sum); // Print the result
    }
}
